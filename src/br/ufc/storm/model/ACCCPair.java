package br.ufc.storm.model;

public class ACCCPair{
	public ACCCPair(int ac, int cc) {
		super();
		this.ac = ac;
		this.cc = cc;
	}
	int ac;
	int cc;
	public int getAc() {
		return ac;
	}
	public void setAc(int ac) {
		this.ac = ac;
	}
	public int getCc() {
		return cc;
	}
	public void setCc(int cc) {
		this.cc = cc;
	}



}