package br.ufc.storm.model;

public class IntIntPair{
	public IntIntPair(int first, int second) {
		super();
		this.first = first;
		this.second = second;
	}
	int first;
	int second;
	public int getFirst() {
		return first;
	}
	public void setFirst(int ac) {
		this.first = ac;
	}
	public int getSecond() {
		return second;
	}
	public void setSecond(int cc) {
		this.second = cc;
	}



}